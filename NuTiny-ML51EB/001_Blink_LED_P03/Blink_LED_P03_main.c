#include "ML51.H"
uint8_t CW[2]= {0x0,0x1};
uint8_t i;
//----------------------------------------------------------------------------------------------//
void main (void)
{
	//GPIO_SetMode(Port0,BIT3,GPIO_MODE_PUSHPULL);
	P03_PUSHPULL_MODE;
//----------------------------------------------------------------------------------------------//
  while(1)
  {
		for (i=0;i<2;i++)
		{
			P03 = *(CW+i);
			Timer2_Delay(24000000,4,200,1000);
		}
  }
}
